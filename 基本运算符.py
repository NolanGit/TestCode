# 右移：>>,右移一位相当于除以2，右移两位相当于除以4
a = 10
b = a >> 2
print(b)  # 2


# 左移：>>,左移一位相当于乘以2，左移两位相当于乘以4
a = 10
b = a << 1
print(b)  # 20


# 除法：/,结果为浮点数
a = 10
b = 2
print(a / b)  # 5.0


# 地板除：//,结果为去余整数或浮点数（根据具体运算数据）
a = 10.0
b = 3.0
print(a // b)  # 3.0


# 取余：/结果为整数或浮点数（根据具体运算数据）
a = 10.0
b = 3.0
print(a % b)  # 1.0
